	<h2>Directories</h2>
	<p>
		Caballero usees the follwoing directory structure.
	</p>
	
	<dl>

		<dt>/</dt>
		<dd>Root, holds favicons, index.php, and that's it.</dd>

		<dt>/cache</dt>
		<dd>location of static copies -- raw and gzip -- of pages</dd>

		<dt>/config</dt>
		<dd>files containing configuration data. Should all end in .config.php</dd>

		<dt>/txt-docs</dt>
		<dd>text files explaining Caballero defines, directories, file extensions and the like.</dd>

		<dt>/downloads</dt>
		<dd>Where files the user can download are hosted</dd>

		<dt>/errors</dt>
		<dd>.config.php and .content.php files for building error pages.</dd>

		<dt>/images</dt>
		<dd>Content images</dd>

		<dt>/includes</dt>
		<dd>Files that contain static code to be executed directly. Should be ".inc.php"</dd>

		<dt>/libs</dt>
		<dd>Library files containing functions and objects, ".lib.php"</dd>
		
		<dt>/modals</dt>
		<dd>.config.php and .content.php files for modal dialogs</dd>

		<dt>/pages</dt>
		<dd>contains .config.php and .content.php files holding page content.</dd>

		<dt>/templates</dt>
		<dd>unique template directories</dd>

		<dt>/templates/default</dt>
		<dd>the default template.</dd>

		<dt>/template/<var>templateName</var></dt>
		<dd>location of .template.php and various CSS files for a specific template</dd>

		<dt>/template/<var>templateName</var>/fonts</dt>
		<dd>webfonts specific to a template</dd>

		<dt>/template/<var>templateName</var>/images</dt>
		<dd>images specific to a template such as backgrounds</dd>
			
	</dl>

